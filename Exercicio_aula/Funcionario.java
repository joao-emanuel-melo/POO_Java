    public int getTotalFuncionarios(){
        return TotalFuncionarios;
    }

    public void setTotalFuncionarios(int TotalFuncionarios){
        this.TotalFuncionarios = TotalFuncionarios;
    }

    String ID;

    public String getID(){
        return ID;
    }

    public void setID(String ID){
        this.ID = ID;
    }
